        # Загружаем заданные вопросы для текущего игрока
        asked_questions = self.load_asked_questions(player_name)
        available_questions = [q for q in self.questions if q not in asked_questions]

        if not available_questions:
            asked_questions = []  # Сброс вопросов
            available_questions = self.questions[:]
            popup = Popup(
                title="Все вопросы заданы",
                content=Label(text=f"Все вопросы заданы игроку {player_name}. Начинаем новый круг."),
                size_hint=(0.8, 0.4)
            )
            popup.open()

        question = random.choice(available_questions)
        asked_questions.append(question)  # Добавляем вопрос в список заданных
        self.save_asked_questions(player_name, asked_questions)  # Сохраняем обновленный список

        self.question_label.text = question
        self.player_label.text = f"Игрок: {player_name}"

        # Переход к следующему игроку
        self.next_player()

