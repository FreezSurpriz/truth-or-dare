    def load_asked_questions(self, player_name):
        # Безопасное имя файла: убираем пробелы и спецсимволы
        safe_name = "".join(c for c in player_name if c.isalnum() or c in ('_', '-'))
        filename = get_data_path(f"{safe_name}_questions.json")
        try:
            with open(filename, 'r', encoding='utf-8') as file:
                return json.load(file)
        except (FileNotFoundError, json.JSONDecodeError):
            return []

    def save_asked_questions(self, player_name, asked_questions):
        safe_name = "".join(c for c in player_name if c.isalnum() or c in ('_', '-'))
        filename = get_data_path(f"{safe_name}_questions.json")
        with open(filename, 'w', encoding='utf-8') as file:
            json.dump(asked_questions, file, ensure_ascii=False, indent=4)

    def show_question(self, instance):
        players = self.manager.get_screen("menu").players
        current_player = players[self.current_player_index]
