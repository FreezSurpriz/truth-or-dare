def get_data_path(filename):
    """Возвращает путь к файлу в директории данных приложения.
    На Android/iOS использует user_data_dir, на ПК — текущую папку.
    """
    app = App.get_running_app()
    if app is not None:
        return os.path.join(app.user_data_dir, filename)
    return filename

class MainMenuScreen(Screen):
    def __init__(self, **kwargs):
        super().__init__(**kwargs)
        self.players = self.load_players()
        self.layout = BoxLayout(orientation='vertical', padding=10, spacing=10)

        self.title_label = Label(text="Добро пожаловать в 'Правда или вызов'!", font_size=24, size_hint=(1, 0.2))
        self.layout.add_widget(self.title_label)

        self.players_scroll = ScrollView(size_hint=(1, 0.6))
        self.players_list = BoxLayout(orientation='vertical', size_hint_y=None)
        self.players_list.bind(minimum_height=self.players_list.setter('height'))
        self.players_scroll.add_widget(self.players_list)
        self.layout.add_widget(self.players_scroll)

        self.update_player_list()

        self.add_player_button = Button(text="Добавить игрока", size_hint=(1, 0.1))
        self.add_player_button.bind(on_press=self.add_player_popup)
        self.layout.add_widget(self.add_player_button)

        self.start_game_button = Button(text="Начать игру", size_hint=(1, 0.1))
        self.start_game_button.bind(on_press=self.start_game)
        self.layout.add_widget(self.start_game_button)

        self.add_widget(self.layout)

    def load_players(self):
        path = get_data_path('players.json')
        try:
            with open(path, 'r', encoding='utf-8') as file:
                return json.load(file)
        except (FileNotFoundError, json.JSONDecodeError):
            return []

    def save_players(self):
        path = get_data_path('players.json')
        with open(path, 'w', encoding='utf-8') as file:
            json.dump(self.players, file, ensure_ascii=False, indent=4)

    def update_player_list(self):
        self.players_list.clear_widgets()
        for index, player in enumerate(self.players):
            player_label = Label(text=f"{index + 1}. {player['name']} ({player['gender']})", size_hint_y=None, height=40)
            delete_button = Button(text="Удалить", size_hint=(0.2, None), height=40)
            delete_button.bind(on_press=lambda instance, idx=index: self.delete_player(idx))

            player_box = BoxLayout(size_hint_y=None, height=40)
            player_box.add_widget(player_label)
            player_box.add_widget(delete_button)

            self.players_list.add_widget(player_box)

    def add_player_popup(self, instance):
        popup_layout = BoxLayout(orientation='vertical', padding=10, spacing=10)
        name_input = TextInput(hint_text="Имя игрока", multiline=False)
        gender_layout = BoxLayout(size_hint_y=None, height=40, spacing=10)

        male_button = ToggleButton(text="Мужской", group="gender", state="down")
        female_button = ToggleButton(text="Женский", group="gender")
        gender_layout.add_widget(male_button)
        gender_layout.add_widget(female_button)

        def save_player(instance):
            name = name_input.text.strip()
            gender = "Мужской" if male_button.state == "down" else "Женский"
            if name:
                self.players.append({"name": name, "gender": gender, "asked_questions": []})
                self.save_players()
                self.update_player_list()
                popup.dismiss()

        save_button = Button(text="Сохранить")
        save_button.bind(on_press=save_player)

        popup_layout.add_widget(name_input)
        popup_layout.add_widget(gender_layout)
        popup_layout.add_widget(save_button)

        popup = Popup(title="Добавить игрока", content=popup_layout, size_hint=(0.8, 0.5))
        popup.open()

    def delete_player(self, index):
        del self.players[index]
        self.save_players()
        self.update_player_list()

    def start_game(self, instance):
        if not self.players:
            popup = Popup(title="Ошибка", content=Label(text="Добавьте хотя бы одного игрока!"), size_hint=(0.6, 0.4))
            popup.open()
            return
        self.manager.current = "game"

